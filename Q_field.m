function [Q, K] = Q_field(n,m)
Q1=zeros(2,4,n-1,m-1);
Q2=zeros(2,4,n-1,m-1);
Q3=zeros(2,4,n-1,m-1);
Q4=zeros(2,4,n-1,m-1);
K=zeros(2,2,n-1,m-1);
for i=1:n-1
    for j=1:m-1
        Q1(:,:,i,j)=[-1,1,0,0;
                     -1,0,1,0];

        Q2(:,:,i,j)=[-1,1,0,0;
                     0,-1,0,1];
        
        Q3(:,:,i,j)=[0,0,-1,1;
                     -1,0,1,0];
        
        Q4(:,:,i,j)=[0,0,-1,1;
                     0,-1,0,1];
        K= [0,1;-1,0];
    end
end
Q={Q1, Q2, Q3, Q4};
end